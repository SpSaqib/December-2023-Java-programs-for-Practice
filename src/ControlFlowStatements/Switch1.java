package ControlFlowStatements;

public class Switch1 {

	public static void main(String[] args) {
		
		switch('c') 
		{
		case 'a':
			System.out.println("Hello "+'a');
			break;
			
		case 'b':
			System.out.println("Hello "+'b');
			
		case 'c':
			System.out.println("Hello "+'c');
			
		case 'd':
			System.out.println("Hello "+'d');
		
		}
		

	}

}
